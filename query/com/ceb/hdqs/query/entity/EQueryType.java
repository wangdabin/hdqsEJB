package com.ceb.hdqs.query.entity;

public enum EQueryType {
	F0("客户查询"), //
	F1("非客户查询"), //
	F2("监管查询"),//
	F3("无");
	
	private final String display;

	private EQueryType(String display) {
		this.display = display;
	}

	public String getDisplay() {
		return this.display;
	}

	public String toString() {
		return this.display;
	}
}
