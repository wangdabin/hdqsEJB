package com.ceb.hdqs.query.entity;

import java.io.Serializable;

/**
 * 余额连续性验证的实体类,主要记录了查询条件的种类和具体的查询条件的值
 * 
 * @author user
 * 
 */
public class BalanceVerifyEntity implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7984761964058832341L;

	/**
	 * 查询种类
	 */
	private String queryKind;
	/**
	 * 具体的查询条件的值
	 */
	private String value;

	private String queryKindKey;

	public String getQueryKind() {
		return queryKind;
	}

	public String getValue() {
		return value;
	}

	public void setQueryKind(String queryKind) {
		this.queryKind = queryKind;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getQueryKindKey() {
		return queryKindKey;
	}

	public void setQueryKindKey(String queryKindKey) {
		this.queryKindKey = queryKindKey;
	}

}
