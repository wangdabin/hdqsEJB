	@Override
	public String parseToString(String huobdh) {
		StringBuilder item = new StringBuilder();
		item.append(this.getJIAOYM()).append("|");
		item.append(this.getJIOYRQ()).append("|");
		item.append(this.getYNGYJG()).append("|");
		// 2013-11-06 添加格式化输出金额和格式化借贷标记
		item.append(QueryMethodUtils.jiedbzFormat(this.getJIEDBZ())).append("|");
		item.append(QueryMethodUtils.decimalFormat(this.getJIO1JE(), huobdh)).append("|");
		item.append(QueryMethodUtils.decimalFormat(this.getZHHUYE(), huobdh)).append("|");
		item.append(this.getZHYODM() == null ? "" : this.getZHYODM()).append("|");
		item.append(this.getPNGZHH() == null ? "" : this.getPNGZHH()).append("|");
		item.append(this.getKEHUZH() == null ? "" : this.getKEHUZH()).append("|");
		item.append(this.getJIO1GY() == null ? "" : this.getJIO1GY()).append("|");
		item.append(this.getSHOQGY() == null ? "" : this.getSHOQGY()).append("|");
		item.append(this.getJIOYSJ() == null ? "" : this.getJIOYSJ()).append("|");
		item.append(this.getCHBUBZ() == null ? "" : this.getCHBUBZ()).append("|");
		item.append(this.getZHUJRQ() == null ? "" : this.getZHUJRQ()).append("|");
		item.append(this.getGUIYLS() == null ? "" : this.getGUIYLS()).append("|");
		item.append(this.getDUIFZH() == null ? "" : this.getDUIFZH()).append("|");
		item.append(this.getDUIFMC() == null ? "" : this.getDUIFMC()).append("|");
		item.append(this.getXNZHBZ() == null ? "" : this.getXNZHBZ());

		return item.toString();
	}

